function [F,theta] = AngularSpringForce(X,K,theta0,RMother,RDaughter,DX)
    Nx = length(X)/2;
    tau_m = RMother*DX*X(1:Nx,:);
    tau_d = RDaughter*DX*X(Nx+1:end,:);
    theta = acos(dot(tau_m/norm(tau_m),tau_d/norm(tau_d)));
    DEDTau = DE_DTau(K,theta0,tau_m,tau_d);
    F = -[DEDTau(1:3)'.*(RMother*DX)';DEDTau(4:6)'.*(RDaughter*DX)'];
end

function Force = DE_DTau(K,theta0,tm,td)
    td1 = td(1);
    td2 = td(2);
    td3 = td(3);
    tm1 = tm(1);
    tm2 = tm(2); 
    tm3 = tm(3);
    
    t2 = abs(td1);
    t3 = abs(td2);
    t4 = abs(td3);
    t5 = abs(tm1);
    t6 = abs(tm2);
    t7 = abs(tm3);
    t8 = conj(td1);
    t9 = conj(td2);
    t10 = conj(td3);
    t11 = conj(tm1);
    t12 = conj(tm2);
    t13 = conj(tm3);
    t14 = t2.^2;
    t15 = t3.^2;
    t16 = t4.^2;
    t17 = t5.^2;
    t18 = t6.^2;
    t19 = t7.^2;
    t20 = t8.*td1;
    t21 = t9.*td2;
    t22 = t10.*td3;
    t23 = t8+td1;
    t24 = t9+td2;
    t25 = t10+td3;
    t26 = t11.*tm1;
    t27 = t12.*tm2;
    t28 = t13.*tm3;
    t29 = t11+tm1;
    t30 = t12+tm2;
    t31 = t13+tm3;
    t32 = 1.0./sqrt(t20);
    t33 = 1.0./sqrt(t21);
    t34 = 1.0./sqrt(t22);
    t35 = 1.0./sqrt(t26);
    t36 = 1.0./sqrt(t27);
    t37 = 1.0./sqrt(t28);
    t38 = t14+t15+t16;
    t39 = t17+t18+t19;
    t40 = 1.0./sqrt(t38);
    t42 = 1.0./sqrt(t39);
    t41 = t40.^3;
    t43 = t42.^3;
    t44 = t11.*t40.*t42.*td1;
    t45 = t12.*t40.*t42.*td2;
    t46 = t13.*t40.*t42.*td3;
    t47 = t44+t45+t46;
    t48 = acos(t47);
    t49 = t47.^2;
    t50 = -t48;
    t51 = -t49;
    t52 = t50+theta0;
    t54 = t51+1.0;
    t53 = sin(t52);
    t55 = 1.0./sqrt(t54);
    mt1 = [-K.*t53.*t55.*(-t40.*t42.*td1+(t5.*t11.*t29.*t35.*t40.*t43.*td1)./2.0+(t5.*t12.*t29.*t35.*t40.*t43.*td2)./2.0+(t5.*t13.*t29.*t35.*t40.*t43.*td3)./2.0);-K.*t53.*t55.*(-t40.*t42.*td2+(t6.*t11.*t30.*t36.*t40.*t43.*td1)./2.0+(t6.*t12.*t30.*t36.*t40.*t43.*td2)./2.0+(t6.*t13.*t30.*t36.*t40.*t43.*td3)./2.0);-K.*t53.*t55.*(-t40.*t42.*td3+(t7.*t11.*t31.*t37.*t40.*t43.*td1)./2.0+(t7.*t12.*t31.*t37.*t40.*t43.*td2)./2.0+(t7.*t13.*t31.*t37.*t40.*t43.*td3)./2.0);-K.*t53.*t55.*(-t11.*t40.*t42+(t2.*t11.*t23.*t32.*t41.*t42.*td1)./2.0+(t2.*t12.*t23.*t32.*t41.*t42.*td2)./2.0+(t2.*t13.*t23.*t32.*t41.*t42.*td3)./2.0)];
    mt2 = [-K.*t53.*t55.*(-t12.*t40.*t42+(t3.*t11.*t24.*t33.*t41.*t42.*td1)./2.0+(t3.*t12.*t24.*t33.*t41.*t42.*td2)./2.0+(t3.*t13.*t24.*t33.*t41.*t42.*td3)./2.0);-K.*t53.*t55.*(-t13.*t40.*t42+(t4.*t11.*t25.*t34.*t41.*t42.*td1)./2.0+(t4.*t12.*t25.*t34.*t41.*t42.*td2)./2.0+(t4.*t13.*t25.*t34.*t41.*t42.*td3)./2.0)];
    Force = [mt1;mt2];
    Force(isnan(Force))=0;
end
